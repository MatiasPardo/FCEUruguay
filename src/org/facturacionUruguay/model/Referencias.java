package org.facturacionUruguay.model;

public class Referencias {

	private Integer tipoCFEReferencia;
	
	private String serieCFEReferencia;
	
	private Integer numeroCFEReferencia;
	
	private String fechaCFEReferencia;

	public Integer getTipoCFEReferencia() {
		return tipoCFEReferencia;
	}

	public void setTipoCFEReferencia(Integer tipoCFEReferencia) {
		this.tipoCFEReferencia = tipoCFEReferencia;
	}

	public String getSerieCFEReferencia() {
		return serieCFEReferencia;
	}

	public void setSerieCFEReferencia(String serieCFEReferencia) {
		this.serieCFEReferencia = serieCFEReferencia;
	}

	public Integer getNumeroCFEReferencia() {
		return numeroCFEReferencia;
	}

	public void setNumeroCFEReferencia(Integer numeroCFEReferencia) {
		this.numeroCFEReferencia = numeroCFEReferencia;
	}

	public String getFechaCFEReferencia() {
		return fechaCFEReferencia;
	}

	public void setFechaCFEReferencia(String fechaCFEReferencia) {
		this.fechaCFEReferencia = fechaCFEReferencia;
	}
	
}
